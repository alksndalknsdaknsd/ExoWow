function ExoWow:ConfigureMonkBindings()
  if ExoWow.spec == 'Mistweaver' then
    ExoWow:SetOverrideBindingClick("ALT-SHIFT-1", "BT4Button49")
    ExoWow:SetOverrideBindingClick("E", "BT4Button50")
    ExoWow:SetOverrideBindingClick("ALT-4", "BT4Button52")
    ExoWow:SetOverrideBindingClick("ALT-SHIFT-5", "BT4Button53")
    ExoWow:SetOverrideBindingClick("ALT-SHIFT-6", "BT4Button54")

    ExoWow:SetOverrideBindingClick("F", "BT4Button57")
    ExoWow:SetOverrideBindingClick("V", "BT4Button58")
    ExoWow:SetOverrideBindingClick("SHIFT-V", "BT4Button59")
    ExoWow:SetOverrideBindingClick("Q", "BT4Button60")
  end
end
